# Simple Python script
def main():
    print("Hello, World!")          # Basic message
    print("Welcome to Python!")     # Extra message
    print("Let's do more things...")# Another message

    # Performing a small calculation
    num1, num2 = 5, 3
    print(f"The sum of {num1} and {num2} is: {num1 + num2}")

    # Loop example
    for i in range(3):
        print(f"Loop iteration {i+1}")

if __name__ == "__main__":
    main()
